<?php

if ( ! defined( 'ABSPATH' ) ) exit;

class GravityForms_Save_Between_Pages {

	/**
	 * The single instance of GravityForms_Save_Between_Pages.
	 * @var 	object
	 * @access  private
	 * @since 	1.0.0
	 */
	private static $_instance = null;

	/**
	 * The version number.
	 * @var     string
	 * @access  public
	 * @since   1.0.0
	 */
	public $_version;

	/**
	 * The token.
	 * @var     string
	 * @access  public
	 * @since   1.0.0
	 */
	public $_token;

	/**
	 * The main plugin file.
	 * @var     string
	 * @access  public
	 * @since   1.0.0
	 */
	public $file;

	/**
	 * The main plugin directory.
	 * @var     string
	 * @access  public
	 * @since   1.0.0
	 */
	public $dir;

	/**
	 * Constructor function.
	 * @access  public
	 * @since   1.0.0
	 * @return  void
	 */
	public function __construct ( $file = '', $version = '1.0.0' ) {
		$this->_version = $version;
		$this->_token = 'gravityForms_save_between_pages';

		// Load plugin environment variables
		$this->file = $file;
		$this->dir = dirname( $this->file );

		// Load API for generic admin functions
		if (class_exists("GFForms")) {
			$this->api = new GravityForms_Save_Between_Pages_API();
		}

		register_activation_hook( $this->file, array( $this, 'install' ) );
	} // End __construct ()

	/**
	 * Main GravityForms_Save_Between_Pages Instance
	 *
	 * Ensures only one instance of GravityForms_Save_Between_Pages is loaded or can be loaded.
	 *
	 * @since 1.0.0
	 * @static
	 * @see GravityForms_Save_Between_Pages()
	 * @return Main GravityForms_Save_Between_Pages instance
	 */
	public static function instance ( $file = '', $version = '1.0.0' ) {
		if ( is_null( self::$_instance ) ) {
			self::$_instance = new self( $file, $version );
		}
		return self::$_instance;
	} // End instance ()

	/**
	 * Cloning is forbidden.
	 *
	 * @since 1.0.0
	 */
	public function __clone () {
		_doing_it_wrong( __FUNCTION__, __( 'Cheatin&#8217; huh?' ), $this->_version );
	} // End __clone ()

	/**
	 * Unserializing instances of this class is forbidden.
	 *
	 * @since 1.0.0
	 */
	public function __wakeup () {
		_doing_it_wrong( __FUNCTION__, __( 'Cheatin&#8217; huh?' ), $this->_version );
	} // End __wakeup ()

	/**
	 * Installation. Runs on activation.
	 * @access  public
	 * @since   1.0.0
	 * @return  void
	 */
	public function install () {
		$this->_log_version_number();
	} // End install ()

	/**
	 * Log the plugin version number.
	 * @access  public
	 * @since   1.0.0
	 * @return  void
	 */
	private function _log_version_number () {
		update_option( $this->_token . '_version', $this->_version );
	} // End _log_version_number ()

}